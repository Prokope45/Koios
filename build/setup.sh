#!/bin/bash
# =============================================================================
# Koios Setup Script
# Detects host OS and configures the build environment accordingly.
# Generates build/docker-compose.override.yml and build/.env.build for
# platform-specific settings (GPU on Windows/Linux with NVIDIA,
# Docker Model Runner on macOS).
#
# This script is intended to be run from the project root via the Makefile.
# =============================================================================

set -e

# Resolve project root (one level up from the build/ directory)
PROJECT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
OVERRIDE_FILE="$PROJECT_ROOT/build/docker-compose.override.yml"
ENV_BUILD_FILE="$PROJECT_ROOT/build/.env.build"

echo "============================================"
echo "  Koios - Platform Detection & Setup"
echo "============================================"

# ─────────────────────────────────────────────
# Helper: configure GPU (Windows / Linux)
# ─────────────────────────────────────────────
configure_gpu() {
    echo "Checking for NVIDIA GPU..."

    if command -v nvidia-smi &>/dev/null && nvidia-smi &>/dev/null; then
        echo "✓ NVIDIA GPU detected."

        cat > "$ENV_BUILD_FILE" <<EOF
# Auto-generated by build/setup.sh — GPU platform
INSTALL_GPU_PACKAGES=true
EOF

        cat > "$OVERRIDE_FILE" <<EOF
# Auto-generated by build/setup.sh — GPU platform (Windows/Linux with NVIDIA)
version: '3.8'

services:
  ollama:
    deploy:
      resources:
        reservations:
          devices:
            - driver: nvidia
              count: all
              capabilities: [gpu]
    environment:
      - OLLAMA_NUM_GPU=99
EOF

        echo "✓ GPU configuration written."
    else
        echo "⚠ No NVIDIA GPU detected (or nvidia-smi not available)."
        echo "  Falling back to CPU-only configuration."

        cat > "$ENV_BUILD_FILE" <<EOF
# Auto-generated by build/setup.sh — no GPU detected
INSTALL_GPU_PACKAGES=false
EOF

        cat > "$OVERRIDE_FILE" <<EOF
# Auto-generated by build/setup.sh — no GPU detected
version: '3.8'

services:
  ollama:
    deploy: {}
    environment:
      - OLLAMA_NUM_GPU=0
EOF

        echo "✓ CPU-only configuration written."
    fi
}

# Detect OS
OS="$(uname -s)"
echo "Detected OS: $OS"
echo ""

# Clean up any previous generated files
rm -f "$OVERRIDE_FILE" "$ENV_BUILD_FILE"

# ─────────────────────────────────────────────
# macOS
# ─────────────────────────────────────────────
if [ "$OS" = "Darwin" ]; then
    echo "Platform: macOS"
    echo ""

    # Check if Docker Desktop model runner is enabled
    echo "Checking Docker Desktop model runner..."
    MODEL_RUNNER_STATUS=$(docker desktop model-runner status 2>/dev/null || echo "not-found")

    if echo "$MODEL_RUNNER_STATUS" | grep -qi "enabled\|running\|active"; then
        echo "✓ Docker Desktop model runner is already enabled."
    else
        echo "Docker Desktop model runner is not enabled."
        echo "Enabling Docker Desktop model runner on TCP port 12434..."
        if docker desktop enable model-runner --tcp 12434 2>/dev/null; then
            echo "✓ Docker Desktop model runner enabled on port 12434."
        else
            echo "⚠ Could not automatically enable Docker Desktop model runner."
            echo "  Please run manually: docker desktop enable model-runner --tcp 12434"
        fi
    fi

    echo ""
    echo "Generating macOS configuration (no GPU passthrough)..."

    cat > "$ENV_BUILD_FILE" <<EOF
# Auto-generated by build/setup.sh — macOS platform
INSTALL_GPU_PACKAGES=false
EOF

    cat > "$OVERRIDE_FILE" <<EOF
# Auto-generated by build/setup.sh — macOS platform
# GPU passthrough is not used on macOS.
version: '3.8'

services:
  ollama:
    deploy: {}
    environment:
      - OLLAMA_NUM_GPU=0
EOF

    echo "✓ macOS configuration written."

# ─────────────────────────────────────────────
# Windows (Git Bash / WSL / MSYS)
# ─────────────────────────────────────────────
elif echo "$OS" | grep -qiE "MINGW|MSYS|CYGWIN|Windows_NT"; then
    echo "Platform: Windows"
    echo ""
    configure_gpu

# ─────────────────────────────────────────────
# Linux (and WSL2 reporting as Linux)
# ─────────────────────────────────────────────
elif [ "$OS" = "Linux" ]; then
    echo "Platform: Linux"
    echo ""
    configure_gpu

else
    echo "⚠ Unknown platform: $OS — defaulting to no-GPU configuration."

    cat > "$ENV_BUILD_FILE" <<EOF
# Auto-generated by build/setup.sh — unknown platform
INSTALL_GPU_PACKAGES=false
EOF

    cat > "$OVERRIDE_FILE" <<EOF
# Auto-generated by build/setup.sh — unknown platform
version: '3.8'

services:
  ollama:
    deploy: {}
    environment:
      - OLLAMA_NUM_GPU=0
EOF
fi

echo ""
echo "============================================"
echo "  Setup complete. Ready to build."
echo "============================================"
